    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
       float temp1,temp2;
              jTextArea1.append("fraction used/n");
        for (int x =0;x < Num;x++){
             fractionUsed = fractionUsed + vec1.get(x);
                jTextArea1.append(fractionUsed+" ="+ fractionUsed + vec1.get(x)+"\n");
                         if(vec2.get(x)==0){
                 UsedDivSpeedUpSum = UsedDivSpeedUpSum+0;
                          jTextArea2.append(UsedDivSpeedUpSum +"="+ UsedDivSpeedUpSum+"0\n");
                         }
             else  
                         {
             UsedDivSpeedUpSum = UsedDivSpeedUpSum + vec1.get(x)/vec2.get(x); 
                           jTextArea2.append( UsedDivSpeedUpSum+" ="+ UsedDivSpeedUpSum +"+"+ vec1.get(x)+"/"+vec2.get(x)+"\n");
                         }
                     }        
               endSpeedUp = 1/ (1-(fractionUsed/100) + UsedDivSpeedUpSum);
                 jTextArea3.append("the final speed up \n= 1/ (1-("+fractionUsed+"/100) +"+ UsedDivSpeedUpSum+")\n="+endSpeedUp);        
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
   String speedup = JOptionPane.showInputDialog(" Enter the required performance increase ");
      float speedUpNum = Float.parseFloat(speedup); 
         String fractUsed = JOptionPane.showInputDialog(" enter the percentage of system not delayed");
      float fractUsedNum = Float.parseFloat(fractUsed);
       float speedUpFactor = (speedUpNum-(fractUsedNum/100)*speedUpNum)/(1-(fractUsedNum/100)*speedUpNum);
       jTextArea4.append("("+speedUpNum+"-"+"("+fractUsedNum+"/100)*"+speedUpNum+")/(1-("+fractUsedNum+"/100)*"+speedUpNum+")\n");
       jTextArea4.append("the speed up ="+speedUpFactor);
       
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         String speedup = JOptionPane.showInputDialog(" Enter the required performance increase ");
      float speedUpNum = Float.parseFloat(speedup); 
         String speedUpratio = JOptionPane.showInputDialog(" enter the speed up ratio");
      float speedUpRatioUsedNum = Float.parseFloat(speedUpratio);
      float FractUsedNum = (speedUpRatioUsedNum-speedUpNum)/(speedUpRatioUsedNum*speedUpNum-speedUpNum);
      jTextArea5.append("("+speedUpRatioUsedNum+"-"+speedUpNum+")/("+speedUpRatioUsedNum+"*"+speedUpNum+"-"+speedUpNum+")\n");
         jTextArea5.append("the speed up ratio ="+FractUsedNum);
      
    }//GEN-LAST:event_jButton4ActionPerformed

