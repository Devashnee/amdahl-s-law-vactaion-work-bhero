    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       String NumOfEn = JOptionPane.showInputDialog(" Enter the number of enhancements done to the system");
       Num = Integer.parseInt(NumOfEn);        
      for (int count = 1;count <= Num;count++){
           String usedParts  = JOptionPane.showInputDialog("enter used parts"+count);   
           jTextArea6.append(usedParts+"  ");
           float temp = Integer.parseInt(usedParts);           
           vec1.add(temp);
           String speedUp  = JOptionPane.showInputDialog("enter speedUp"+count); 
            jTextArea6.append(speedUp+"\n");           
           float temp2 = Integer.parseInt(speedUp);
               vec2.add(temp2);      
              }           
    }//GEN-LAST:event_jButton3ActionPerformed

